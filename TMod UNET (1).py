    # Concatenate all predictions and ground truths
    all_preds = np.concatenate(all_preds, axis=0)
    all_gts   = np.concatenate(all_gts, axis=0)

    # 4. Compute confusion matrix (raw counts)
    cm = confusion_matrix(all_gts, all_preds, labels=CLASS_LABELS)

    # 5. Optional: Normalize the confusion matrix by true label counts
    # cm_norm = cm.astype('float') / cm.sum(axis=1, keepdims=True)
    # We'll use raw counts or you can choose to normalize

    # 6. Plot confusion matrix
    disp = ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=CLASS_LABELS)
    fig, ax = plt.subplots(figsize=(6, 6))
    disp.plot(ax=ax, cmap=plt.cm.Blues, values_format='g')
    ax.set_title(f"Confusion Matrix for {config_name}")
    plt.show()

