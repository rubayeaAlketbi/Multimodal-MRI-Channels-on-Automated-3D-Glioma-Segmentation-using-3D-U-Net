def build_unet(num_channels, NUM_CLASSES=4, dropout=0.2):
    inputs = layers.Input(shape=(128, 128, 128, num_channels))

    # Encoder Block 0
    conv0 = layers.Conv3D(16, (3, 3, 3), padding='same')(inputs)
    conv0 = layers.BatchNormalization()(conv0)
    conv0 = layers.ReLU()(conv0)
    conv0 = layers.Conv3D(16, (3, 3, 3), padding='same')(conv0)
    conv0 = layers.BatchNormalization()(conv0)
    conv0 = layers.ReLU()(conv0)

    pool1 = layers.MaxPooling3D(pool_size=(2, 2, 2))(conv0)

    # Encoder Block 1
    conv1 = layers.Conv3D(32, (3, 3, 3), padding='same')(pool1)
    conv1 = layers.BatchNormalization()(conv1)
    conv1 = layers.ReLU()(conv1)
    conv1 = layers.Conv3D(32, (3, 3, 3), padding='same')(conv1)
    conv1 = layers.BatchNormalization()(conv1)
    conv1 = layers.ReLU()(conv1)

    pool2 = layers.MaxPooling3D(pool_size=(2, 2, 2))(conv1)

    # Encoder Block 2
    conv2 = layers.Conv3D(64, (3, 3, 3), padding='same')(pool2)
    conv2 = layers.BatchNormalization()(conv2)
    conv2 = layers.ReLU()(conv2)
    conv2 = layers.Conv3D(64, (3, 3, 3), padding='same')(conv2)
    conv2 = layers.BatchNormalization()(conv2)
    conv2 = layers.ReLU()(conv2)

    pool3 = layers.MaxPooling3D(pool_size=(2, 2, 2))(conv2)

    # Encoder Block 3
    conv3 = layers.Conv3D(128, (3, 3, 3), padding='same')(pool3)
    conv3 = layers.BatchNormalization()(conv3)
    conv3 = layers.ReLU()(conv3)
    conv3 = layers.Conv3D(128, (3, 3, 3), padding='same')(conv3)
    conv3 = layers.BatchNormalization()(conv3)
    conv3 = layers.ReLU()(conv3)

    pool4 = layers.MaxPooling3D(pool_size=(2, 2, 2))(conv3)

    # Encoder Block 4
    conv4 = layers.Conv3D(256, (3, 3, 3), padding='same')(pool4)
    conv4 = layers.BatchNormalization()(conv4)
    conv4 = layers.ReLU()(conv4)
    conv4 = layers.Conv3D(256, (3, 3, 3), padding='same')(conv4)
    conv4 = layers.BatchNormalization()(conv4)
    conv4 = layers.ReLU()(conv4)

    pool5 = layers.MaxPooling3D(pool_size=(2, 2, 2))(conv4)

    # Bottleneck
    conv5 = layers.Conv3D(512, (3, 3, 3), padding='same')(pool5)
    conv5 = layers.BatchNormalization()(conv5)
    conv5 = layers.ReLU()(conv5)
    conv5 = layers.Conv3D(512, (3, 3, 3), padding='same')(conv5)
    conv5 = layers.BatchNormalization()(conv5)
    conv5 = layers.ReLU()(conv5)
    drop5 = layers.Dropout(dropout)(conv5)

    # Decoder Block 1
    up6 = layers.UpSampling3D(size=(2, 2, 2))(drop5)
    up6 = layers.Conv3D(256, (2, 2, 2), padding='same')(up6)
    up6 = layers.BatchNormalization()(up6)
    up6 = layers.ReLU()(up6)
    merge6 = layers.concatenate([conv4, up6])
    conv6 = layers.Conv3D(256, (3, 3, 3), padding='same')(merge6)
    conv6 = layers.BatchNormalization()(conv6)
    conv6 = layers.ReLU()(conv6)
    conv6 = layers.Conv3D(256, (3, 3, 3), padding='same')(conv6)
    conv6 = layers.BatchNormalization()(conv6)
    conv6 = layers.ReLU()(conv6)

    # Decoder Block 2
    up7 = layers.UpSampling3D(size=(2, 2, 2))(conv6)
    up7 = layers.Conv3D(128, (2, 2, 2), padding='same')(up7)
    up7 = layers.BatchNormalization()(up7)
    up7 = layers.ReLU()(up7)
    merge7 = layers.concatenate([conv3, up7])
    conv7 = layers.Conv3D(128, (3, 3, 3), padding='same')(merge7)
    conv7 = layers.BatchNormalization()(conv7)
    conv7 = layers.ReLU()(conv7)
    conv7 = layers.Conv3D(128, (3, 3, 3), padding='same')(conv7)
    conv7 = layers.BatchNormalization()(conv7)
    conv7 = layers.ReLU()(conv7)

    # Decoder Block 3
    up8 = layers.UpSampling3D(size=(2, 2, 2))(conv7)
    up8 = layers.Conv3D(64, (2, 2, 2), padding='same')(up8)
    up8 = layers.BatchNormalization()(up8)
    up8 = layers.ReLU()(up8)
    merge8 = layers.concatenate([conv2, up8])
    conv8 = layers.Conv3D(64, (3, 3, 3), padding='same')(merge8)
    conv8 = layers.BatchNormalization()(conv8)
    conv8 = layers.ReLU()(conv8)
    conv8 = layers.Conv3D(64, (3, 3, 3), padding='same')(conv8)
    conv8 = layers.BatchNormalization()(conv8)
    conv8 = layers.ReLU()(conv8)

    # Decoder Block 4
    up9 = layers.UpSampling3D(size=(2, 2, 2))(conv8)
    up9 = layers.Conv3D(32, (2, 2, 2), padding='same')(up9)
    up9 = layers.BatchNormalization()(up9)
    up9 = layers.ReLU()(up9)
    merge9 = layers.concatenate([conv1, up9])
    conv9 = layers.Conv3D(32, (3, 3, 3), padding='same')(merge9)
    conv9 = layers.BatchNormalization()(conv9)
    conv9 = layers.ReLU()(conv9)
    conv9 = layers.Conv3D(32, (3, 3, 3), padding='same')(conv9)
    conv9 = layers.BatchNormalization()(conv9)
    conv9 = layers.ReLU()(conv9)

    # Decoder Block 5
    up10 = layers.UpSampling3D(size=(2, 2, 2))(conv9)
    up10 = layers.Conv3D(16, (2, 2, 2), padding='same')(up10)
    up10 = layers.BatchNormalization()(up10)
    up10 = layers.ReLU()(up10)
    merge10 = layers.concatenate([conv0, up10])
    conv10 = layers.Conv3D(16, (3, 3, 3), padding='same')(merge10)
    conv10 = layers.BatchNormalization()(conv10)
    conv10 = layers.ReLU()(conv10)
    conv10 = layers.Conv3D(16, (3, 3, 3), padding='same')(conv10)
    conv10 = layers.BatchNormalization()(conv10)
    conv10 = layers.ReLU()(conv10)

    # Output Layer
    outputs = layers.Conv3D(NUM_CLASSES, (1, 1, 1), activation='softmax')(conv10)

    return Model(inputs=inputs, outputs=outputs)

"""## Loss Functions
In this part of the code, the aim is to define loss functions and metrics used in performing the following tasks. These mertics are used in order to perform comparative analysis on each version of the model. The implementation of the loss functions where obtained from the following github repository :  https://github.com/keras-team/keras/issues/9395
"""

# Dice loss as defined above for 4 classes
def dice_coef(y_true, y_pred, smooth=1.0):
    class_num = 4
    for i in range(class_num):
        y_true_f = K.flatten(y_true[:,:,:,:,i])
        y_pred_f = K.flatten(y_pred[:,:,:,:,i])
        intersection = K.sum(y_true_f * y_pred_f)
        loss = ((2. * intersection + smooth) / (K.sum(y_true_f) + K.sum(y_pred_f) + smooth))
        if i == 0:
            total_loss = loss
        else:
            total_loss = total_loss + loss

    total_loss = total_loss / class_num
    return total_loss

# Defining loss functions of dice per classes
def dice_coef_necrotic(y_true, y_pred, epsilon=1e-4):
    intersection = K.sum(K.abs(y_true[:,:,:,:,1] * y_pred[:,:,:,:,1]))
    return (2. * intersection) / (K.sum(K.square(y_true[:,:,:,:,1])) + K.sum(K.square(y_pred[:,:,:,1])) + epsilon)

def dice_coef_edema(y_true, y_pred, epsilon=1e-4):
    intersection = K.sum(K.abs(y_true[:,:,:,:,2] * y_pred[:,:,:,:,2]))
    return (2. * intersection) / (K.sum(K.square(y_true[:,:,:,:,2])) + K.sum(K.square(y_pred[:,:,:,2])) + epsilon)

def dice_coef_enhancing(y_true, y_pred, epsilon=1e-4):
    intersection = K.sum(K.abs(y_true[:,:,:,:,3] * y_pred[:,:,:,:,3]))
    return (2. * intersection) / (K.sum(K.square(y_true[:,:,:,:,3])) + K.sum(K.square(y_pred[:,:,:,3])) + epsilon)


# Defining Precision
def precision(y_true, y_pred):
        true_positives = K.sum(K.round(K.clip(y_true * y_pred, 0, 1)))
        predicted_positives = K.sum(K.round(K.clip(y_pred, 0, 1)))
        precision = true_positives / (predicted_positives + K.epsilon())
        return precision


# Defining Sensitivity
def sensitivity(y_true, y_pred):
    true_positives = K.sum(K.round(K.clip(y_true * y_pred, 0, 1)))
    possible_positives = K.sum(K.round(K.clip(y_true, 0, 1)))
    return true_positives / (possible_positives + K.epsilon())


# Defining Specificity
def specificity(y_true, y_pred):
    true_negatives = K.sum(K.round(K.clip((1-y_true) * (1-y_pred), 0, 1)))
    possible_negatives = K.sum(K.round(K.clip(1-y_true, 0, 1)))
    return true_negatives / (possible_negatives + K.epsilon())

# Defining Tversky_loss
def tversky_loss(y_true, y_pred, alpha= 0.7, beta= 0.3, smooth=1e-6):
    y_true_f = K.flatten(y_true)
    y_pred_f = K.flatten(y_pred)
    # Compute TP, FP, FN
    TP = K.sum(y_true_f * y_pred_f)
    FP = K.sum(y_pred_f * (1-y_true_f))
    FN = K.sum((1-y_pred_f) * y_true_f)
    # Compute Tversky
    Tversky = (TP + smooth) / (TP + alpha*FN + beta*FP + smooth)
    return 1 - Tversky

# Defining Focal Loss
def focal_loss(y_true, y_pred, gamma=2.0, alpha=0.25):
    y_pred = K.clip(y_pred, K.epsilon(), 1.0 - K.epsilon())
    # Compute the focal loss term for each pixel and each class
    focal_loss_tensor = -y_true * alpha * K.pow(1 - y_pred, gamma) * K.log(y_pred)
    # Sum the loss over classes (typically the last axis)
    return K.sum(focal_loss_tensor, axis=-1)

