"""## Data Generators
To efficiently handle the loading of large 3D MRI volumes during training, data generators are used. These generators load the data in batches, apply any necessary preprocessing (such as normalization and cropping), and feed the data into the model on the fly.

This approach prevents the need to load the entire dataset into memory at once, which is especially important when working with high-dimensional medical imaging data. The data generator is also responsible for applying data augmentation techniques during training to increase variability and help the model generalize better.

Separate generators are defined for the training, validation, and testing sets to ensure that:


*   Augmentation is applied only to the training data.

*   Validation and testing data remain unchanged for consistent evaluation.




"""

import tensorflow as tf
import numpy as np
import os
import nibabel as nib
from skimage.transform import resize
import matplotlib.pyplot as plt
from scipy import ndimage
import cv2

class DataGenerator(tf.keras.utils.Sequence):

    def __init__(self, list_IDs, chosen_mods=['flair', 't1ce', 't2'], batch_size=1,
                 dim=(128,128,128), n_classes=4, shuffle=True, augment=True):
        self.list_IDs = list_IDs
        self.chosen_mods = chosen_mods
        self.batch_size = batch_size
        self.dim = dim
        self.n_classes = n_classes
        self.shuffle = shuffle
        self.augment = augment
        self.indexes = np.arange(len(self.list_IDs))
        self.augment_count = 0
        self.augment_limit = 20 # Maximum Number of Samples to Augment
        self.on_epoch_end()

    def __len__(self):
        # Number of batches per epoch.
        return int(np.floor(len(self.list_IDs) / self.batch_size))

    def __getitem__(self, index):
        # Generate indexes of the batch
        indexes = self.indexes[index*self.batch_size:(index+1)*self.batch_size]
        batch_IDs = [self.list_IDs[k] for k in indexes]
        X, y = self.__data_generation(batch_IDs)
        return X, y

    def on_epoch_end(self):
        #Shuffles indexes after each epoch.
        if self.shuffle:
            np.random.shuffle(self.indexes)
        # Set the augment counter to 0 on end of every epoch
        self.augment_count = 0

    def load_mods(self, patient_folder, patient_id, chosen_mods):
        #Load and return a stacked 3D volume for the chosen modalities.
        mod_data = []
        for mod in chosen_mods:
            file_path = os.path.join(patient_folder, f"{patient_id}_{mod}.nii")
            img = nib.load(file_path)
            data = img.get_fdata()  # load data once
            mod_data.append(data)
        # Stack the data along the last dimension
        stacked_data = np.stack(mod_data, axis=-1)
        return stacked_data

    def __data_generation(self, batch_IDs):
        # Generates data containing batch_size samples with at least one tumor slice.
        num_channels = len(self.chosen_mods)
        X = np.empty((self.batch_size, *self.dim, num_channels), dtype=np.float32)
        y = np.empty((self.batch_size, *self.dim, self.n_classes), dtype=np.float32)
        valid_count = 0

        for i, ID in enumerate(batch_IDs):
            pat_path = os.path.join(TRAIN_DATASET_PATH, ID)
            # Call load_mods now works correctly because of self
            combined_data = self.load_mods(pat_path, ID, self.chosen_mods)
            mask_path  = os.path.join(pat_path, f"{ID}_seg.nii")

            if not os.path.exists(mask_path):
                print(f"⚠️ Skipping missing mask for patient: {ID}")
                continue

            # Load and process the mask
            mask = nib.load(mask_path).get_fdata()
            mask[mask == 4] = 3  # Fix segmentation labels if necessary
            mask = tf.one_hot(mask.astype(np.uint8), depth=self.n_classes)

            # Apply normalization and cropping (assuming these functions are defined)
            processed_data = normalize_scan(combined_data).astype(np.float32)
            processed_data = crop_brain_region(processed_data)
            processed_mask = crop_brain_region(mask)

            # Ensure at least one sample with a tumor is in the batch
            if np.any(processed_mask[..., 1:]):  # Check if tumor is present
              # If the augmentation is enabled and the limit is not exceeded, augment the sample
              if self.augment_count and self.augment < self.augment_limit:
                processed_data, processed_mask = self.augment_data(np.expand_dims(processed_data, axis=0),
                                                                   np.expand_dims(processed_mask, axis=0))
                # Remove the batch dimension added above
                processed_data = processed_data[0]
                processed_mask = processed_mask[0]
                self.augment_count += 1

              # Add the valid processed data to the batch
              X[valid_count] = processed_data
              y[valid_count] = processed_mask
              valid_count += 1
            else:
              print(f"⚠️ Skipping sample without tumor for patient: {ID}")

        # If no valid samples, return an empty batch to avoid crashes
        if valid_count == 0:
            print("⚠️ No tumor-containing data in batch, returning empty batch.")
            return np.zeros_like(X), np.zeros_like(y)

        return X[:valid_count], y[:valid_count]


    def augment_data(self, imgs, masks):
      """
      Apply real-time data augmentation to a batch of images and masks.

      Args:
        imgs: A NumPy array of images with shape (batch_size, D, H, W, C)
        masks: A NumPy array of masks with shape (batch_size, D, H, W, n_classes)

      Returns:
        Augmented imgs and masks with the same target shape.
      """
      # Create arrays to hold the augmented data
      augmented_imgs = np.empty_like(imgs)
      augmented_masks = np.empty_like(masks)

      # Process each sample in the batch individually
      for i in range(imgs.shape[0]):
        img = imgs[i]
        mask = masks[i]

        # Adding random Gaussian noise
        if np.random.rand() < 0.1:
            noise = np.random.normal(0, 0.01, img.shape)
            img = np.clip(img + noise, 0, 1)

        # Adding random translations (shift in x, y, and z)
        if np.random.rand() < 0.1:
            shift_x, shift_y, shift_z = np.random.randint(-10, 10, 3)
            # For a single sample, shape is (D, H, W, C)
            img = ndimage.shift(img, (shift_x, shift_y, shift_z, 0), mode='nearest')
            mask = ndimage.shift(mask, (shift_x, shift_y, shift_z, 0), mode='nearest')

        # Adding random scaling
        if np.random.rand() < 0.1:
            scale = np.random.uniform(0.9, 1.1)
            # Since each sample is 4D, use a tuple of 4 factors: (scale, scale, scale, 1)
            scale_factors = (scale, scale, scale, 1)
            img = ndimage.zoom(img, scale_factors, order=1)
            mask = ndimage.zoom(mask, scale_factors, order=0)

        # Adding random flips along the horizontal axis (axis=1)
        if np.random.rand() < 0.1:
            img = np.flip(img, axis=1)
            mask = np.flip(mask, axis=1)

        # Generate different views (e.g., rotation for axial, sagittal, coronal views)
        if np.random.rand() < 0.1:
            img, mask = self.generate_different_views(img, mask)

        # After augmentation, force the sample to resize back to the target shape.
        # The target shape for images is defined as self.dim + (num_channels,)
        target_shape_img = self.dim + (img.shape[-1],)
        # For masks, the target shape is self.dim + (mask.shape[-1],)
        target_shape_mask = self.dim + (mask.shape[-1],)

        if img.shape != target_shape_img:
            img = resize(img, target_shape_img, preserve_range=True, anti_aliasing=True).astype(np.float32)
        if mask.shape != target_shape_mask:
            mask = resize(mask, target_shape_mask, preserve_range=True, anti_aliasing=False).astype(np.float32)

        augmented_imgs[i] = img
        augmented_masks[i] = mask

      return augmented_imgs, augmented_masks


    def generate_different_views(self, img, mask):
        '''
        Generate different views (Axial, Sagittal, Coronal).
        '''
        axis_choice = np.random.choice([0, 1, 2])
        img = np.rot90(img, k=np.random.randint(1, 4), axes=(axis_choice, (axis_choice + 1) % 3))
        mask = np.rot90(mask, k=np.random.randint(1, 4), axes=(axis_choice, (axis_choice + 1) % 3))
        return img, mask

